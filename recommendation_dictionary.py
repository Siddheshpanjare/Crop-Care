def get_recommendation(predicted_class, severity_level, language="en"):
    """
    Returns the recommendation for the given predicted class and severity level.
    """
    if language == "English":
        lang_code = "en"
    elif language == "Hindi":
        lang_code = "hi"
    else:
        lang_code = language
        
    if predicted_class in DISEASE_DB:
        db_entry = DISEASE_DB[predicted_class]
        lang_entry = db_entry.get(lang_code, db_entry.get("en", {}))
        return lang_entry.get(severity_level, "No specific recommendation available.")
    else:
        return "Disease not found in recommendation database. Please consult an expert."
